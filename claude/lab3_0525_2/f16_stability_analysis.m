%% f16_stability_analysis.m — F-16 纵向开环稳定性分析函数
%==========================================================================
%  功能:
%    对给定的 A 矩阵 (5×5 纵向状态矩阵) 进行开环稳定性分析:
%      1. 计算特征值 (开环极点)
%      2. 对每个特征值: 自然频率 ω_n, 阻尼比 ζ, 时间常数 τ
%      3. 分类: 短周期模态 vs 长周期 (沉浮) 模态 vs 发动机模态
%      4. 判定: 渐进稳定 / 不稳定
%
%  开环稳定性判定标准:
%    所有特征值实部 Re(λ) < 0 → 渐进稳定 (扰动后自动回到配平)
%    存在 Re(λ) > 0 → 不稳定 (扰动后发散)
%    Re(λ)=0 → 临界稳定 (等幅振荡, 工程上也视为不稳定)
%
%  模态分类 (基于特征值形态):
%    共轭复根 → 振荡模态 (短周期或长周期)
%       短周期: |λ| 较大 (ω_n≈3), 周期短, 阻尼可调
%       长周期: |λ| 较小 (ω_n≈0.1), 周期长, 弱阻尼
%    实根 → 非周期模态 (发动机或螺旋)
%
%  输入参数:
%    A           — 5×5 开环状态矩阵 (来自 f16_longitudinal_model)
%    model_label — 可选, 模型名称字符串 (用于显示)
%
%  输出:
%    无直接返回值, 结果打印到命令窗口
%
%  用法:
%    [A,~,~,~,~,~,label] = f16_longitudinal_model('LOFI');
%    f16_stability_analysis(A, label);
%
%  参考:
%    Stevens, Lewis "Aircraft Control and Simulation" Ch.3
%    模态分类方法基于飞行力学标准教材
%==========================================================================

function f16_stability_analysis(A, model_label)
% 输入参数处理: model_label 可选, 默认空字符串
if nargin<2, model_label=''; end

% 打印分析标题
fprintf('\n============================================================\n');
fprintf('  纵向开环稳定性分析');
if ~isempty(model_label), fprintf(' — %s', model_label); end
fprintf('\n============================================================\n');

% --- 计算特征值 ---
% eig(A) 返回 A 矩阵的 n 个特征值 (复数向量)
% 对于 5×5 纵向 A 矩阵, 得到 5 个特征值
eig_vals = eig(A);
fprintf('\n  【特征值】\n');

% --- 遍历每个特征值, 计算模态参数并分类 ---
for i=1:length(eig_vals)
    ev=eig_vals(i);        % 当前特征值 (复数)
    wn=abs(ev);            % 自然频率 ω_n = |λ| = sqrt(Re²+Im²)
    % 阻尼比 ζ = -Re/|λ| (仅对振荡模态有意义)
    % max(wn,1e-10) 防止除以零 (实特征值 wn 可能为零)
    zeta=-real(ev)/max(wn,1e-10);

    if abs(imag(ev))<1e-6
        % 实特征值 (虚部≈0): 非周期模态
        % τ = -1/Re(λ): 时间常数 (秒), 表示收敛/发散速度
        % 仅对实特征值有意义: 3τ 后衰减到 5%, 5τ 后衰减到 1%
        fprintf('    λ%d = %+.4f           (ω_n=%.4f, ζ=%.4f, τ=%.2fs)\n',...
            i,real(ev),wn,zeta,-1/real(ev));
    elseif wn<0.1
        % 低频振荡 (ω_n < 0.1 rad/s): 长周期 (Phugoid) 模态
        % 物理来源: 势能-动能交换, 飞机沿波浪轨迹飞行
        % 特点: 周期长 (~60s), 阻尼弱, 对飞行员操控影响小
        fprintf('    λ%d = %+.4f%+.4fi (ω_n=%.4f, ζ=%.4f) ← 长周期(Phugoid)\n',...
            i,real(ev),imag(ev),wn,zeta);
    else
        % 高频振荡 (ω_n ≥ 0.1): 短周期模态
        % 物理来源: 迎角变化→升力变化→俯仰力矩→迎角恢复
        % 特点: 周期短 (~2s), 阻尼是飞行品质的关键指标
        %   ζ=0.7: 最佳阻尼 (飞行品质 Level 1)
        %   ζ<0.3: 阻尼不足, 飞行员难以操控 (Level 3)
        %   ζ<0:   负阻尼, 不稳定 (发散振荡)
        fprintf('    λ%d = %+.4f%+.4fi (ω_n=%.4f, ζ=%.4f) ← 短周期\n',...
            i,real(ev),imag(ev),wn,zeta);
    end
end

% --- 综合稳定性判据 ---
reals=real(eig_vals);       % 所有特征值的实部
imags=abs(imag(eig_vals));  % 所有特征值虚部的绝对值

fprintf('\n  【稳定性判据】 ');
if all(reals<0)
    % 所有实部为负 → 渐进稳定
    % 物理含义: 在配平点受到扰动后, 飞机自动返回配平状态
    fprintf('✅ 渐进稳定 (所有Re(λ)<0)\n');

    % 计算最差阻尼比 (用于评估飞行品质)
    % 只对振荡模态 (|λ|>0.1) 计算, 忽略发动机实极点
    zeta_min=min(-reals./max(abs(eig_vals),1e-10));
    fprintf('    最差阻尼: ζ_min=%.2f',zeta_min);
    if zeta_min<0.3
        % ζ<0.3 属于 Level 2/3 飞行品质, 需要反馈补偿
        fprintf(' ⚠ 不足，需反馈改善\n');
    else
        fprintf(' ✅ 充足\n');
    end
else
    % 存在实部≥0的特征值 → 不稳定
    % 需要控制器 (反馈) 来镇定系统
    fprintf('❌ 不稳定! Re≥0 特征值: %d个\n',sum(reals>=0));
end
fprintf('\n');
end
